const month="April"
switch (month) {
    case "jan":
        console.log("January");
        break;

    case "feb":
        console.log("Feb");
        break;

    case "mar":
        console.log("march");
        break;

    case "apr":
        console.log("April");
        break;

    default:
        console.log("default case match");
        break;
        
        
}